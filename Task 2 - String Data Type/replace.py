# Save the sentence "The!quick!brown!fox!jumps!over!the!lazy!dog!."
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog!."

# Replace all the "!" in the sentence the "space" character

sentence = sentence.replace("!", "")

# Print the sentence without any "!"

print(sentence)

# Print sentence in upper case

print(sentence.upper())

# print sentence in reverse by using [::-1]

print(sentence[::-1])